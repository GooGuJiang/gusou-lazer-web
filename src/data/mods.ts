export const getModDefinition = (acronym: string): ModDefinition | undefined => {
  const normalizedAcronym = acronym.trim().toUpperCase();
  return MOD_DEFINITIONS.find((definition) => definition.acronym === normalizedAcronym);
};

export const getModTypeColor = (type: ModType | undefined): string => {
  if (!type) return MOD_TYPE_COLORS.None;
  return MOD_TYPE_COLORS[type];
};
